<h2>Chaos</h2>
<p>
  Single-user randomized intent fuzzer. Drives the canvas through 200+ random patches /
  mutations; checks invariants between each. Multi-user invariants land post-Stage B.
</p>
<pre><code>flox activate -- task chaos:run</code></pre>

<h2>CI shape</h2>
<p>
  The full gate runs in <code>.github/workflows/ci.yml</code> inside Flox so the toolchain (Node
  22, Faust 2.85.5, esbuild) matches local. Postgres 17 is a service container; the DB schema is
  applied before <code>task ci</code> runs.
</p>
