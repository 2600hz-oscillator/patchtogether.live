<h2>E2E — Playwright</h2>
<p>
  Headless Chromium, real Web Audio (worklets and all), against the actual SvelteKit app. Boots a
  Vite dev server (or <code>vite preview</code> when <code>E2E_USE_PREVIEW=1</code>). Lives under
  <code>e2e/tests/</code>; specs tagged <code>@smoke</code> double as the post-deploy live check
  on autotest.
</p>
<pre><code>flox activate -- task e2e          # full suite
flox activate -- task e2e:headed   # with the browser visible
flox activate -- task ci:smoke:live  # smoke vs autotest.patchtogether.live</code></pre>

