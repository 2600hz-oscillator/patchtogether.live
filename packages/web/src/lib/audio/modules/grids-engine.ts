export const GRIDS_NUM_PARTS = 3; // BD, SD, HH
export const GRIDS_STEPS_PER_PATTERN = 32;
/** Output modes. */
export const GRIDS_MODE_DRUMS = 1;
export const GRIDS_MODE_EUCLIDEAN = 0;

// ----------------------------------------------------------------------------
// avrlib fixed-point helpers (avrlib/op.h), reproduced exactly.
// ----------------------------------------------------------------------------

/** 8x8 -> 16 multiply, then >> 8. `(a * b) >> 8`, a,b in 0..255. */
export function u8u8MulShift8(a: number, b: number): number {
  return ((a & 0xff) * (b & 0xff)) >> 8;
}

/**
 * Linear interpolation between two 0..255 bytes by an 0..255 `balance`.
 * avrlib U8Mix(a, b, balance) = (a * (255 - balance) + b * balance) >> 8.
 * balance is masked to a byte so callers can pass `x << 2` (which on the AVR
 * is an implicit uint8_t wrap) — we replicate that wrap with `& 0xff`.
 */
export function u8Mix(a: number, b: number, balance: number): number {
  const bal = balance & 0xff;
  return (((a & 0xff) * (255 - bal)) + ((b & 0xff) * bal)) >> 8;
}

// ----------------------------------------------------------------------------
// Drum map: the 5x5 grid of node indices. (drum_map[5][5] in
// pattern_generator.cc — note the original is column-major-ish; we preserve
// the exact node ordering.)
// ----------------------------------------------------------------------------

export const GRIDS_DRUM_MAP: readonly (readonly number[])[] = [
  [10, 8, 0, 9, 11],
  [15, 7, 13, 12, 6],
  [18, 14, 4, 5, 3],
  [23, 16, 21, 1, 2],
  [24, 19, 17, 20, 22],
];

// ----------------------------------------------------------------------------
// ReadDrumMap — bilinear interpolation of an instrument's step level across
// the four map nodes surrounding (x, y). x, y are 0..255. Mirrors
// PatternGenerator::ReadDrumMap exactly.
// ----------------------------------------------------------------------------

export function readDrumMap(step: number, instrument: number, x: number, y: number): number {
  const i = x >> 6; // 0..3 (the >>6 of a 0..255 byte yields 0..3, never 4)
  const j = y >> 6;
  const aMap = GRIDS_NODES[GRIDS_DRUM_MAP[i]![j]!]!;
  const bMap = GRIDS_NODES[GRIDS_DRUM_MAP[i + 1]![j]!]!;
  const cMap = GRIDS_NODES[GRIDS_DRUM_MAP[i]![j + 1]!]!;
  const dMap = GRIDS_NODES[GRIDS_DRUM_MAP[i + 1]![j + 1]!]!;
  const offset = instrument * GRIDS_STEPS_PER_PATTERN + step;
  const a = aMap[offset]!;
  const b = bMap[offset]!;
  const c = cMap[offset]!;
  const d = dMap[offset]!;
  return u8Mix(u8Mix(a, b, x << 2), u8Mix(c, d, x << 2), y << 2);
}

// ----------------------------------------------------------------------------
// Euclidean pattern read. length in 1..32 (the upstream `(len>>3)+1` mapping
// happens at the host), density 0..31. Returns true if a pulse fires on
// `euclideanStep` of `length`.
// ----------------------------------------------------------------------------

export function euclideanBit(length: number, density: number, euclideanStep: number): boolean {
  const len = Math.max(1, Math.min(32, length));
  const den = Math.max(0, Math.min(31, density));
  const address = (len - 1) * 32 + den;
  const patternBits = GRIDS_EUCLIDEAN[address]! >>> 0;
  const stepInPattern = euclideanStep % len;
  // Use >>> to read the bit without sign issues at step 31.
  return ((patternBits >>> stepInPattern) & 1) === 1;
}

// ----------------------------------------------------------------------------
// Output state bit layout (drums, output_clock=false — the standalone module
// uses the per-instrument + per-instrument-accent layout):
//   bit0 BD   bit1 SD   bit2 HH
//   bit3 BDAC bit4 SDAC bit5 HHAC   (accents, shifted << 3)
// ----------------------------------------------------------------------------

export const GRIDS_BIT_BD = 1 << 0;
export const GRIDS_BIT_SD = 1 << 1;
export const GRIDS_BIT_HH = 1 << 2;
export const GRIDS_BIT_BD_ACCENT = 1 << 3;
export const GRIDS_BIT_SD_ACCENT = 1 << 4;
export const GRIDS_BIT_HH_ACCENT = 1 << 5;

export interface GridsDrumSettings {
  /** MAP-X, 0..255. */
  x: number;
  /** MAP-Y, 0..255. */
  y: number;
  /** CHAOS / randomness, 0..255. */
  randomness: number;
  /** Per-instrument density 0..255 (BD, SD, HH). */
  density: [number, number, number];
}

export interface GridsEuclideanSettings {
  /** Per-instrument euclidean length 0..255 (mapped to 1..32 by (>>3)+1). */
  length: [number, number, number];
  /** Per-instrument density 0..255 (mapped to 0..31 by >>3). */
  density: [number, number, number];
}

/**
 * Evaluate the DRUMS pattern for a single step. Returns the 6-bit state
 * (3 triggers + 3 accents). `partPerturbation` is the per-channel CHAOS offset
 * (0..255), recomputed by the caller at the start of each pattern (step 0).
 * Faithful to PatternGenerator::EvaluateDrums (output_clock=false path).
 */
export function evaluateDrums(
  step: number,
  s: GridsDrumSettings,
  partPerturbation: readonly [number, number, number],
): number {
  let state = 0;
  let accentBits = 0;
  let instrumentMask = 1;
  for (let i = 0; i < GRIDS_NUM_PARTS; i++) {
    let level = readDrumMap(step, i, s.x, s.y);
    const pert = partPerturbation[i]!;
    if (level < 255 - pert) {
      level += pert;
    } else {
      level = 255;
    }
    // density 0..255; threshold = ~density (bitwise NOT on a byte = 255-density)
    const threshold = (~s.density[i]!) & 0xff;
    if (level > threshold) {
      if (level > 192) {
        accentBits |= instrumentMask;
      }
      state |= instrumentMask;
    }
    instrumentMask <<= 1;
  }
  state |= accentBits << 3;
  return state;
}

/**
 * Compute the per-channel CHAOS perturbation for a fresh pattern. Mirrors the
 * step==0 block in EvaluateDrums: `part_perturbation_[i] =
 * U8U8MulShift8(Random::GetByte(), randomness >> 2)`. `randByte()` must return
 * a 0..255 pseudo-random byte; pass a seedable PRNG for determinism in tests.
 */
export function computePerturbation(
  randomness: number,
  randByte: () => number,
): [number, number, number] {
  const r = (randomness >> 2) & 0xff;
  return [
    u8u8MulShift8(randByte(), r),
    u8u8MulShift8(randByte(), r),
    u8u8MulShift8(randByte(), r),
  ];
}

/**
 * Evaluate the EUCLIDEAN pattern for a single instrument index at a given
 * euclidean step. Mirrors EvaluateEuclidean's per-instrument body. Returns
 * { fire, reset } where reset is true when euclideanStep wraps to 0.
 */
export function evaluateEuclidean(
  instrument: number,
  s: GridsEuclideanSettings,
  euclideanStep: number,
): { fire: boolean; reset: boolean } {
  const length = (s.length[instrument]! >> 3) + 1; // 1..32
  const density = s.density[instrument]! >> 3; // 0..31
  const stepInPattern = euclideanStep % length;
  const fire = euclideanBit(length, density, stepInPattern);
  return { fire, reset: stepInPattern === 0 };
}

/**
 * Deterministic xorshift32 PRNG returning bytes 0..255 — used for CHAOS so
 * the host (and tests) get reproducible perturbation given a seed. The
 * original uses avrlib Random (a 32-bit LFSR); xorshift is a faithful-enough
 * stand-in for a "white" byte stream (Grids' randomness is perceptual, not
 * cryptographic, and the exact LFSR taps are not musically load-bearing).
 */
export function makeByteRng(seed: number): () => number {
  let state = seed >>> 0 || 0x1d872b41;
  return () => {
    state ^= state << 13; state >>>= 0;
    state ^= state >>> 17;
    state ^= state << 5; state >>>= 0;
    return (state >>> 16) & 0xff;
  };
}

